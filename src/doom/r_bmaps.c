// -----------------------------------------------------------------------------
// [crispy] brightmaps for states
// -----------------------------------------------------------------------------

const byte *R_BrightmapForState (const int state)
{
    if (vis_brightmaps)
    {
        switch (state)
        {
            case S_BFG1:
            case S_BFG2:
            case S_BFG3:
            case S_BFG4:
            {
                return redonly;
                break;
            }
        }
	}

    return nobrightmap;
}
